jQuery(document).ready(function() {
  $('.themkinhnghiem').click(function(){
    $('.form_themkinhnghiem').toggle();
  });
  $('.tab-dangky').click(function(){
    $('.tab-hocvan').removeClass("active");
    $('.tab-kynang').removeClass("active");
    $('.tab-hoso').removeClass("active");
    $('.tab-ngonngu').removeClass("active");
    $('.tab-kinhnghiem').removeClass("active");
    $('.tab-ungvien').removeClass("active");
    $('.hocvan').css("display","none");
    $('.kynang').css("display","none");
    $('.ungvien').css("display","none");
    $('.ngonngu').css("display","none");
    $('.ungvien').css("display","none");
    $('.kinhnghiem').css("display","none");
    $('.hoso').css("display","none");
    $('.dangky').css("display","block");
    $('.tab-dangky').addClass("active");
  });
  $('.tab-hocvan').click(function(){
    $('.tab-hocvan').addClass("active");
    $('.tab-kynang').removeClass("active");
    $('.tab-hoso').removeClass("active");
    $('.tab-ngonngu').removeClass("active");
    $('.tab-kinhnghiem').removeClass("active");
    $('.tab-ungvien').removeClass("active");
    $('.hocvan').css("display","block");
    $('.kynang').css("display","none");
    $('.ungvien').css("display","none");
    $('.ngonngu').css("display","none");
    $('.ungvien').css("display","none");
    $('.kinhnghiem').css("display","none");
    $('.hoso').css("display","none");
    $('.dangky').css("display","none");
    $('.tab-dangky').removeClass("active");
  });
});
